function normalize(w) {
    return w.toLowerCase().trim()
        .replace(/^(ο|η|το|οι|τα|τους|τις)\s+/u, '')
        .trim();
}

function isPopular(greekWord) {
    const candidates = new Set();
    const add = w => {
        candidates.add(w.trim().toLowerCase());
        candidates.add(normalize(w));
    };
    add(greekWord);
    for (const p of greekWord.split('/')) add(p.trim());
    for (const p of greekWord.split('|')) add(p.trim());
    for (const p of greekWord.split(',')) add(p.trim());

    for (const c of candidates) {
        if (POPULAR.has(c) || POPULAR.has(normalize(c))) return true;
        // Проверяем с артиклем
        for (const art of ['ο ', 'η ', 'το ']) {
            if (POPULAR.has(art + c)) return true;
        }
    }
    return false;
}

let content = fs.readFileSync(LESSONS_FILE, 'utf8');

let totalWords     = 0;
let markedWords    = 0;
let alreadyMarked  = 0;
const markedList   = [];

const wordObjRegex = /\{[^{}]*greek:\s*(['"])(.*?)\1[^{}]*\}/gs;

const newContent = content.replace(wordObjRegex, (match) => {
    totalWords++;
    const greekMatch = match.match(/greek:\s*(['"])(.*?)\1/s);
    if (!greekMatch) return match;
    const greek = greekMatch[2];

    if (/\bpopular\s*:/.test(match)) { alreadyMarked++; return match; }

    if (isPopular(greek)) {
        markedWords++;
        markedList.push(greek);
        return match.replace(/\s*\}$/, ', popular: true }');
    }
    return match;
});

fs.writeFileSync(LESSONS_FILE, newContent, 'utf8');

